def translate_options(options: list[str], lang: str) -> list[str]:
    """
    선택지 리스트를 주어진 언어로 번역해서 반환합니다.
    """
    vt = VALUE_TRANSLATIONS.get(lang, {})
    return [vt.get(opt, opt) for opt in options]

def get_translation(lang: str, key: str) -> str:
    """
    UI 텍스트 키를 주어진 언어로 번역해서 반환합니다.
    """
    return TRANSLATIONS.get(lang, TRANSLATIONS["ko"]).get(key, key)

def get_value_translation(lang: str, value: str) -> str:
    """
    선택값(예: 국가/언어/관심 분야)을 주어진 언어로 번역해서 반환합니다.
    """
    return VALUE_TRANSLATIONS.get(lang, {}).get(value, value)

def reverse_value_translation(lang: str, value: str) -> str:
    """
    번역된 값을 내부 키(한국어)로 역변환해서 반환합니다.
    """
    vt = VALUE_TRANSLATIONS.get(lang, {})
    reverse_map = {v: k for k, v in vt.items()}
    return reverse_map.get(value, value)
